string1 = str(input('Please input a string: '))
print('Length of the string:', len(string1))
print('First letter of the string:', string1[0])
print('Last letter of the string:', string1[-1])
print('Upper case of the string:', string1.upper())
print('Lower case of the string:', string1.lower())
print('The first two words of the string:', string1[:2])
print('The last two words of the string:', string1[-2:])

